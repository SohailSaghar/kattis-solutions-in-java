    //digit sum to 1 digit
    public static int digitSumto1(int n)
    {
        int sum = 0;

        // Loop to do sum while
        // sum is not less than
        // or equal to 9
        while (n > 0 || sum > 9)
        {
            if (n == 0) {
                n = sum;
                sum = 0;
            }
            sum += n % 10;
            n /= 10;
        }
        return sum;
    }
    //Digitsum
    public static int calculateDigitSum(int number) {
        int digitSum = 0;

        while (number > 0) {
            digitSum = digitSum + (number % 10);
            number = number / 10;
        }

        return digitSum;
    }

